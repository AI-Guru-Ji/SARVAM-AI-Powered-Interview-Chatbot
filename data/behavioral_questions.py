def get_behavioral_questions_for_role(role: str) -> list[dict]:
    """Return the list of 5 behavioral questions, applying any role
    overrides defined in ROLE_SPECIFIC_BEHAVIORAL_OVERRIDES."""
    base = list(BEHAVIORAL_QUESTIONS)
    overrides = ROLE_SPECIFIC_BEHAVIORAL_OVERRIDES.get(role, {})
    for idx, replacement in overrides.items():
        if 0 <= idx < len(base):
            base[idx] = replacement
    return base
